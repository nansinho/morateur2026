<h2>Notre stratégie : une offensive sur trois fronts</h2>
<p><a href="/candidat">Mathieu Morateur</a> propose un plan de lutte ambitieux qui combine <strong>action publique exemplaire</strong>, <strong>mobilisation citoyenne</strong> et <strong>innovation écologique</strong>.</p>

<h3>Front 1 : Exemplarité sur l''espace public</h3>
<ul>
<li><strong>Traitement des berges des vallats</strong> chaque printemps avec des solutions biologiques (Bti)</li>
<li>Pose de <strong>bornes aspirateurs à moustiques</strong> autour des points stratégiques : écoles, espaces verts, équipements sportifs</li>
<li>Entretien renforcé des espaces publics pour éliminer toute eau stagnante</li>
</ul>

<h3>Front 2 : Mobilisation des citoyens</h3>
<ul>
<li>Campagne de sensibilisation pour <strong>assécher les gîtes privés</strong> (coupelles, gouttières, récipients)</li>
<li><strong>Tarifs négociés</strong> pour les dispositifs anti-moustiques à installer dans les jardins</li>
<li>Accompagnement technique par les services municipaux</li>
</ul>

<h3>Front 3 : Solutions écologiques innovantes</h3>
<ul>
<li>Déploiement de <strong>pièges biologiques</strong> sur l''ensemble du territoire</li>
<li>Introduction de <strong>prédateurs naturels</strong> (chauves-souris, libellules) via des nichoirs et habitats dédiés</li>
<li>Piégeage ciblé des femelles reproductrices</li>
</ul>

<h2>La lutte contre les rats : même rigueur</h2>
<p>Le problème ne se limite pas aux moustiques. La prolifération des <strong>rats</strong> dans certains quartiers appelle des mesures tout aussi fermes :</p>
<ul>
<li>Pose de <strong>pièges à rats</strong> dans les espaces publics</li>
<li>Mesures coercitives contre les propriétaires laissant s''installer des <strong>troubles à la salubrité</strong></li>
</ul>

<h2>Une réglementation municipale stricte</h2>
<p>L''ensemble de ces mesures sera encadré par une <strong>réglementation municipale stricte</strong>, avec des contrôles réguliers et des sanctions pour garantir l''efficacité du dispositif.</p>
